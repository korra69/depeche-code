the_weather = rainy

case the_weather
when 'rainy'
  puts "Bring your boots."
when 'sunny'
  puts "Break out the snazzy sunhat."
when 'foggy'
  puts "Another fashionalbe trench day."
when 'snowing'
  puts "Cool! Snowball fight after lunch!"
when 'windy'
  puts "Wear a sweater and your beanie."
else
  puts "Your best guess. Bring a jacket and umbrella just in case."
end 
